"""
Cluster stability diagnostics for the framework correlation clustering.

Implements three validations that reviewer 6 asked for, none of which
change the numbers already reported in the paper but all of which give
the reader a way to check that the three-cluster structure is not a
visual-inspection artefact.

1. Bootstrap co-assignment frequency.
   Resample the 200 samples with replacement B times. For each bootstrap
   resample, rebuild the 20x20 correlation matrix, cluster it with Ward's
   method at k=3, and record a binary co-assignment matrix. Average across
   resamples yields a framework-by-framework stability matrix whose entry
   (i, j) is the empirical probability that frameworks i and j end up in
   the same cluster.

2. Silhouette and gap statistic for k in {2, ..., 8}.
   Runs the same Ward procedure at multiple k and reports silhouette
   width and the gap statistic of Tibshirani, Walther and Hastie (2001).
   This replaces the visual elbow with a quantitative choice.

3. Alternative linkage comparison.
   Runs average and complete linkage at k=3 and reports the adjusted
   Rand index between each and Ward. Stable structure should not depend
   strongly on the linkage rule.
"""

from __future__ import annotations

import numpy as np
import pandas as pd
from typing import Dict, List, Sequence

from scipy.cluster.hierarchy import linkage, fcluster
from scipy.spatial.distance import squareform
from sklearn.metrics import adjusted_rand_score, silhouette_score


def _corr_distance(corr: np.ndarray) -> np.ndarray:
    dist = np.sqrt(np.maximum(0.0, 2.0 * (1.0 - corr)))
    np.fill_diagonal(dist, 0.0)
    return dist


def _ward_labels(corr: np.ndarray, k: int) -> np.ndarray:
    dist = _corr_distance(corr)
    condensed = squareform(dist, checks=False)
    Z = linkage(condensed, method="ward")
    return fcluster(Z, t=k, criterion="maxclust")


def _linkage_labels(corr: np.ndarray, k: int, method: str) -> np.ndarray:
    dist = _corr_distance(corr)
    condensed = squareform(dist, checks=False)
    Z = linkage(condensed, method=method)
    return fcluster(Z, t=k, criterion="maxclust")


def bootstrap_coassignment(
    df: pd.DataFrame,
    primary_cols: Sequence[str],
    n_boot: int = 1000,
    k: int = 3,
    seed: int = 20260409,
) -> pd.DataFrame:
    """
    Return a framework-by-framework matrix of co-assignment probabilities.
    """
    rng = np.random.default_rng(seed)
    m = len(primary_cols)
    counts = np.zeros((m, m), dtype=float)
    n = len(df)
    sub = df[list(primary_cols)].to_numpy()

    successful = 0
    for _ in range(n_boot):
        idx = rng.integers(0, n, size=n)
        resample = sub[idx]
        # Skip degenerate resamples where a column became constant
        stds = resample.std(axis=0, ddof=1)
        if np.any(stds < 1e-12):
            continue
        corr = np.corrcoef(resample, rowvar=False)
        if np.any(~np.isfinite(corr)):
            continue
        labels = _ward_labels(corr, k=k)
        for i in range(m):
            for j in range(m):
                if labels[i] == labels[j]:
                    counts[i, j] += 1.0
        successful += 1

    if successful == 0:
        raise RuntimeError("All bootstrap resamples were degenerate.")

    probs = counts / successful
    labels = [c.split("_")[0] for c in primary_cols]
    return pd.DataFrame(probs, index=labels, columns=labels)


def silhouette_and_gap(
    df: pd.DataFrame,
    primary_cols: Sequence[str],
    k_range: Sequence[int] = range(2, 9),
    n_reference: int = 20,
    seed: int = 20260409,
) -> pd.DataFrame:
    """
    Compute silhouette width and a correlation-based gap statistic across k.

    The gap statistic compares the within-cluster dispersion W(k) observed
    on the real data to the mean W(k) on uniform reference data. Reference
    datasets are generated by sampling each primary metric uniformly from
    its observed minimum-to-maximum range, which is the classical
    Tibshirani et al. (2001) choice.
    """
    rng = np.random.default_rng(seed)
    sub = df[list(primary_cols)].to_numpy()
    corr = np.corrcoef(sub, rowvar=False)
    dist = _corr_distance(corr)

    def _wk(dist_mat: np.ndarray, labels: np.ndarray) -> float:
        total = 0.0
        for c in np.unique(labels):
            members = np.where(labels == c)[0]
            if len(members) < 2:
                continue
            block = dist_mat[np.ix_(members, members)]
            total += float(np.sum(block[np.triu_indices_from(block, k=1)] ** 2))
        return total

    lo = sub.min(axis=0)
    hi = sub.max(axis=0)

    rows = []
    for k in k_range:
        labels_real = _ward_labels(corr, k=k)
        sil = float(
            silhouette_score(dist, labels_real, metric="precomputed")
        ) if k >= 2 and len(set(labels_real)) > 1 else float("nan")
        wk_real = _wk(dist, labels_real)

        ref_log_wks = []
        for _ in range(n_reference):
            ref = rng.uniform(lo, hi, size=sub.shape)
            ref_corr = np.corrcoef(ref, rowvar=False)
            if np.any(~np.isfinite(ref_corr)):
                continue
            ref_dist = _corr_distance(ref_corr)
            ref_labels = _ward_labels(ref_corr, k=k)
            ref_log_wks.append(np.log(max(_wk(ref_dist, ref_labels), 1e-12)))

        if ref_log_wks:
            mean_ref = float(np.mean(ref_log_wks))
            sd_ref = float(np.std(ref_log_wks, ddof=1)) if len(ref_log_wks) > 1 else 0.0
            gap = mean_ref - np.log(max(wk_real, 1e-12))
            sk = sd_ref * np.sqrt(1.0 + 1.0 / max(len(ref_log_wks), 1))
        else:
            gap = float("nan")
            sk = float("nan")

        rows.append({
            "k": k,
            "silhouette": sil,
            "Wk": wk_real,
            "gap": gap,
            "gap_se": sk,
        })
    return pd.DataFrame(rows)


def linkage_comparison(
    df: pd.DataFrame,
    primary_cols: Sequence[str],
    k: int = 3,
) -> Dict[str, object]:
    """Compare Ward clusters against average and complete linkage."""
    sub = df[list(primary_cols)].to_numpy()
    corr = np.corrcoef(sub, rowvar=False)
    labels_ward = _ward_labels(corr, k=k)

    results: Dict[str, object] = {
        "ward_labels": labels_ward.tolist(),
    }
    for method in ("average", "complete", "single"):
        labels_alt = _linkage_labels(corr, k=k, method=method)
        results[f"{method}_labels"] = labels_alt.tolist()
        results[f"ari_ward_vs_{method}"] = float(
            adjusted_rand_score(labels_ward, labels_alt)
        )
    return results
